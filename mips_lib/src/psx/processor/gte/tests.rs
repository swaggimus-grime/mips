use super::Gte;

#[test]
fn gte_lzcr() {
    let expected = [
        (0x00000000, 32),
        (0xffffffff, 32),
        (0x00000001, 31),
        (0x80000000, 1),
        (0x7fffffff, 1),
        (0xdeadbeef, 2),
        (0x000c0ffe, 12),
        (0xfffc0ffe, 14),
    ];

    let mut gte = Gte::new();

    for &(lzcs, lzcr) in &expected {
        gte.set_data(30, lzcs);

        let r = gte.data(31);

        assert!(r == lzcr);
    }
}

#[test]
fn gte_ops() {
    for test in TESTS {
        println!("Test: '{}'", test.desc);
        println!("Command: 0x{:08x}", test.command);

        let mut gte = test.initial.make_gte();

        gte.command(test.command);

        test.result.validate(gte);
    }
}

struct Test {
    /// Test description
    desc: &'static str,
    /// Initial GTE configuration
    initial: Config,
    /// GTE command being executed
    command: u32,
    /// GTE configuration post-command
    result: Config,
}

/// GTE register config: slice of couples `(register_offset, register_value)`. Missing registers
/// are set to 0.
struct Config {
    /// Control registers
    controls: &'static [(u8, u32)],
    /// Data registers
    data: &'static [(u8, u32)],
}

impl Config {
    fn make_gte(&self) -> Gte {
        let mut gte = Gte::new();

        for &(reg, val) in self.controls {
            gte.set_control(reg, val);
        }

        for &(reg, val) in self.data {
            if reg == 15 {
                // Writing to 14 should set this register and writing here will push a new entry
                // onto the XY_FIFO which will change the previous values.
                continue;
            }

            if reg == 28 {
                // This sets the IR1...3 registers MSB but those values should have been set
                // through registers 9...11
                continue;
            }

            if reg == 29 {
                // This register is read only
                continue;
            }

            gte.set_data(reg, val);
        }

        gte
    }

    fn validate(&self, gte: Gte) {
        let mut error_count = 0u32;

        for &(reg, val) in self.controls {
            let v = gte.control(reg);

            if v != val {
                println!(
                    "Control register {}: expected 0x{:08x} got 0x{:08x}",
                    reg, val, v
                );
                error_count += 1;
            }
        }

        for &(reg, val) in self.data {
            let v = gte.data(reg);

            if v != val {
                println!(
                    "Data register {}: expected 0x{:08x} got 0x{:08x}",
                    reg, val, v
                );
                error_count += 1;
            }
        }

        if error_count > 0 {
            panic!("{} registers errors", error_count);
        }
    }
}

